    private boolean testData()
    {
        boolean ff =true;

        String c_nom = jTextField1.getText() ;
        String c_prenom = jTextField2.getText();
        String c_tel = jTextField3.getText();
        String c_adresse = jTextField5.getText();
        String c_mail = jTextField4.getText();
        
        //***** client *****   
        //nom
        if(!verifier_chaine(c_nom))
        {
            jLabel11.setVisible(true);
            ff = false;
        }
        else
            jLabel11.setVisible(false);
        //prenom
        if(!verifier_chaine(c_prenom))
        {
            jLabel12.setVisible(true);
            ff = false;
        }else
            jLabel12.setVisible(false);
        //telephone
        if(!verifier_8chiffre(c_tel))
        {
            jLabel13.setVisible(true);
            ff = false;
        }else
            jLabel13.setVisible(false);
        //adresse
        if(c_adresse.length()<5||c_adresse.equals("adresse..."))
        {
            jLabel5.setVisible(true);
            ff = false;
        }else
            jLabel15.setVisible(false);
        //email
        if(c_mail.indexOf('@')==-1||c_mail.equals("exemple@exemple.com"))
        {
            jLabel14.setVisible(true);
            ff = false;
        }else if(c_mail.substring(c_mail.indexOf('@'),c_mail.length()).indexOf('.')==-1)
        {
            jLabel14.setVisible(true);
            ff = false;
        }else
            jLabel14.setVisible(false);

        return ff;
    }
    
    
    boolean verifier_chaine(String s)
    {
        if(s.length()==0)
            return false;
        for(int i = 0;i<s.length();i++)
            if(s.charAt(i)>='a'&&s.charAt(i)<='z'||s.charAt(i)>='A'&&s.charAt(i)<='Z') {
            } else {
                return false;
            }
        return true;
    }
    boolean verifier_8chiffre(String c)
    {
        if(c.length()!=8)
            return false;
        try
        {
            Integer.parseInt(c);
        }catch(NumberFormatException e)
        {
            return false;
        }
        
        return true;       
    }
