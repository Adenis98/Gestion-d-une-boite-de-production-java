       public void ClockPane(SimpleDateFormat sdf,LocalDateTime now,String r) throws ParseException {
        Float seconds = Float.parseFloat(now.toString().substring(17,r.toString().length()-2));
        Date deb = sdf.parse(r);
        String dateN = now.toString().substring(0,10)+" "+now.toString().substring(11,now.toString().length());
        Date fin = sdf.parse(dateN);
        long resultat = deb.getTime() - fin.getTime();

        tickTock(resultat,seconds);
        Timer timer = new Timer(500, new ActionListener() {
          @Override
          public void actionPerformed(ActionEvent e) {
              try{
                LocalDateTime now = LocalDateTime.now(); 
                Float seconds = 60 -  (Float.parseFloat(now.toString().substring(17,r.toString().length()-2)));

                Date deb = sdf.parse(r);
                String dateN = now.toString().substring(0,10)+" "+now.toString().substring(11,now.toString().length());
                Date fin = sdf.parse(dateN);
                long resultat = deb.getTime() - fin.getTime();
                tickTock(resultat,seconds);
              }catch(Exception ex)
              {

              }

          }
        });
    timer.setRepeats(true);
    timer.setCoalesce(true);
    timer.setInitialDelay(0);
    timer.start();
  }

  public void tickTock(Long resultat,float s) {
    jLabel37.setText("Temps Restant : "+resultat/86400000+" jours "+(resultat%86400000)/3600000+":"+((resultat%86400000)%3600000)/60000+":"+(int)s);
  }
